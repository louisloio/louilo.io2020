                                <h2 class="m-0">Sneaker War</h2>
                                <h3 class="text-secondary mb-4">Define scenarios and objectives</h3>
                                <h5>Target to win</H5>
                                <p class="text-secondary">The gameplay is based on Candy Crush. 22 different pairs of sneakers are used in the game, the rule is simple. After aligning 3 or more shoes, score the maximum points by blowing them up.</p>
                                <h5>Basic gameplay</h5>
                                <p class="text-secondary">
                                    The duration of each game is limited to 3 minutes.
                                    <br>4, 5 or 6 shoes blow up, a magic ball will appear on the screen and will destroy the sneakers.
                                    <br>4 shoes are destroyed, a bronze magic ball will fade in and destroy 1/3 of the sneakers on the screen.
                                    <br>5 shoes are destroyed, a silver magic ball will fade in and destroy 1/2 of the sneakers on the screen.
                                    <br>6 shoes are destroyed, a gold magic ball will fade in and destroy all the sneakers on the screen.
                                    <br>The basketball could also be created by a square of 4 identical sneakers.</p>
